package modelo.datos.objetos.generales;

public class Item {
    
    String atributo;
    String valor;
    
    public Item() {
        super();
    }


    public void setAtributo(String atributo) {
        this.atributo = atributo;
    }

    public String getAtributo() {
        return atributo;
    }

    public void setValor(String valor) {
        this.valor = valor;
    }

    public String getValor() {
        return valor;
    }
}
